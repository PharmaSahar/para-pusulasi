def get_finding_spec(code: str) -> FindingSpec:
    key = str(code or "").strip()
    if key in _REGISTRY:
        return _REGISTRY[key]
    return _spec(
        key or "unknown_finding",
        "validator_failure",
        "LOW",
        "LOW",
        "Unregistered finding code.",
        "validator",
        "register_finding_code",
        False,
    )


def list_finding_specs() -> list[dict[str, object]]:
    return [item.to_dict() for item in sorted(_REGISTRY.values(), key=lambda x: x.code)]
